<?php

namespace BookEngine\Node;

abstract class Element extends Node
{
    /**
     * @var int
     */
    protected $x;

    /**
     * @var int
     */
    protected $y;

    /**
     * @var int
     */
    protected $zIndex = 0;

    /**
     * @return int
     */
    public function getX()
    {
        return $this->x;
    }

    /**
     * @param int $x
     */
    public function setX($x)
    {
        $this->x = $x;
    }

    /**
     * @return int
     */
    public function getY()
    {
        return $this->y;
    }

    /**
     * @param int $y
     */
    public function setY($y)
    {
        $this->y = $y;
    }

    /**
     * @return int
     */
    public function getZIndex()
    {
        return $this->zIndex;
    }

    /**
     * @param int $zIndex
     */
    public function setZIndex($zIndex)
    {
        $this->zIndex = $zIndex;
    }
}